# Simple PNG files creation script
param()

# Basic 16x16 blue PNG (minimal valid PNG)
$png16 = @(
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,  # IHDR chunk start
    0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10,  # 16x16 dimensions
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x91, 0x68,  # 8-bit RGB, CRC
    0x36, 0x00, 0x00, 0x00, 0x19, 0x49, 0x44, 0x41,  # IDAT chunk start
    0x54, 0x78, 0x9C, 0x62, 0x64, 0x60, 0x60, 0xF8,  # Compressed data
    0xCF, 0xC0, 0xC0, 0xC0, 0x60, 0x60, 0x60, 0x06,
    0x00, 0x00, 0x78, 0x00, 0x35, 0xCF, 0x10, 0x1A,
    0x0E, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E,
    0x44, 0xAE, 0x42, 0x60, 0x82                      # IEND
)

# Write files
[System.IO.File]::WriteAllBytes("icon16.png", [byte[]]$png16)

# Copy for other sizes (Chrome will scale)
Copy-Item "icon16.png" "icon48.png"
Copy-Item "icon16.png" "icon128.png"

Write-Host "Created PNG icon files!"
Write-Host "Files: icon16.png, icon48.png, icon128.png"